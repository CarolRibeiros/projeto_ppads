package br.com.projeto.bens;

public class UsuarioDois {
	private String nome_usuario;
	private String senha;
	private String data_nascimento;
	private String data_ciencia;
	private String estado;
	private String cidade;
	private String salario;
	private String nivelInstrucao;
	private String empresa;
	private String num_cartao;
	private String nome_resp;
	private String dt_validade;
	private String cvc;
	
	
	public String getNome_usuario() {
		return nome_usuario;
	}
	public void setNome_usuario(String nome_usuario) {
		this.nome_usuario = nome_usuario;
	}
	public String getSenha() {
		return senha;
	}
	public void setSenha(String senha) {
		this.senha = senha;
	}
	public String getData_nascimento() {
		return data_nascimento;
	}
	public void setData_nascimento(String data_nascimento) {
		this.data_nascimento = data_nascimento;
	}
	public String getData_ciencia() {
		return data_ciencia;
	}
	public void setData_ciencia(String data_ciencia) {
		this.data_ciencia = data_ciencia;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getCidade() {
		return cidade;
	}
	public void setCidade(String cidade) {
		this.cidade = cidade;
	}
	public String getSalario() {
		return salario;
	}
	public void setSalario(String salario) {
		this.salario = salario;
	}
	public String getNivelInstrucao() {
		return nivelInstrucao;
	}
	public void setNivelInstrucao(String nivelInstrucao) {
		this.nivelInstrucao = nivelInstrucao;
	}
	public String getEmpresa() {
		return empresa;
	}
	public void setEmpresa(String empresa) {
		this.empresa = empresa;
	}
	public String getNum_Cartao() {
		return num_cartao;
	}
	public void setNum_Cartao(String num_cartao) {
		this.num_cartao = num_cartao;
	}
	public String getNome_Resp() {
		return nome_resp;
	}
	public void setNome_Resp(String nome_resp) {
		this.nome_resp = nome_resp;
	}
	public String getDt_Validade() {
		return dt_validade;
	}
	public void setDt_Validade(String dt_validade) {
		this.dt_validade = dt_validade;
	}
	public String getCvc() {
		return cvc;
	}
	public void setCvc(String cvc) {
		this.cvc = cvc;
	}
}
